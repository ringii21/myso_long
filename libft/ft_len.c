int	ft_len(long long int nbr)
{
	int	len;

	len = 0;
	if (nbr == 0)
		return (1);
	else if (nbr == -2147483648)
		return (11);
	else if (nbr < 0)
	{
		nbr = -nbr;
		len++;
	}
	while (nbr > 0)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
